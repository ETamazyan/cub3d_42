#include "libft.h"

void	ft_putendl_fd(char *s, int fd)
{
	size_t	i;
	char	n;

	i = 0;
	if (!s)
		return ;
	while (s[i])
	{
		write (fd, &s[i], 1);
		i++;
	}
	n = '\n';
	write(fd, &n, 1);
}
